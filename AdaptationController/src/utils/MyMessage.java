package utils;

import java.io.Serializable;

import org.apache.mina.transport.socket.nio.NioDatagramSession;


public class MyMessage implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	public NioDatagramSession session;
	public byte[] byteInputStream;
	long id;
	public MyMessage() {
		super();
	}



	public byte[] getByteInputStream() {
		return byteInputStream;
	}



	public void setByteInputStream(byte[] byteInputStream) {
		this.byteInputStream = byteInputStream;
	}



	public long getId() {
		return id;
	}



	public void setId(long id) {
		this.id = id;
	}



	public NioDatagramSession getSession() {
		return session;
	}

	public void setSession(NioDatagramSession session) {
		this.session = session;
	}
	
}
